/**
 *  This macro creates a C++ struct containing the specified member type/name pairs in the var args.
 *  The struct acts as a wrapper around ShaderObject for accessing members of a ShaderStructObject instance through named c++ members.
 *
 *  The macro also defines a nested CPU struct (accessible as name::CPU) with the same
 *  member type/name pairs for easier data management on the cpu side for shader buffer types.
 *
 *  The name::CPU type can be directly used as the cpu side representation
 *  of the shader struct without worrying about padding and alignment.
 *
 *  Object nesting is supported.
 *  Up to 50 member variables are supported.
 *
 *  The getShaderStruct() public static function can be used to retrieve the ShaderStructDef instance.
 *
 *  e.g.:
 *      DefineStruct(MvpBuffer, Mat4f, mvp)
 *
 *      void shader() {
 *          ...
 *          MvpBuffer myBuffer;
 *          myBuffer.mvp = mat4();
 *      }
 *
 * @param name The type name of the struct
 */
#define ShaderStruct(name, ...)\
    struct name {\
        static constexpr char _##name##_type[] = #name;\
        struct CPU {\
            GenerateCpuElementDeclaration(__VA_ARGS__)\
        };\
        static inline xng::ShaderDataType TYPE{static_cast<xng::ShaderStructType>(#name), 1};\
        static xng::ShaderStructDef getShaderStructDef() {\
            return xng::ShaderStructDef(#name, {\
                GenerateStructElements(__VA_ARGS__) \
            });\
        }\
        xng::ShaderScript::ShaderObject _object;\
        GenerateElementDeclaration(__VA_ARGS__)\
        name() : _object(xng::ShaderScript::ShaderStructObject<_##name##_type, 1>()), GenerateConstructor(__VA_ARGS__) { xng::ShaderScript::ShaderScope::get().addTypeDefinition(getShaderStructDef()); }\
        name(const xng::ShaderScript::ShaderObject &buffer, bool b = false) : _object(buffer), GenerateConstructor(__VA_ARGS__) { xng::ShaderScript::ShaderScope::get().addTypeDefinition(getShaderStructDef()); }\
        name& operator=(const name& other) {\
            _object = other._object;\
            return *this;\
        }\
        name& operator=(const xng::ShaderScript::ShaderObject& other) {\
            _object = other;\
            return *this;\
        }\
        operator const xng::ShaderScript::ShaderObject&() const { return _object; }\
    };

