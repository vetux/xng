/**
 * This header defines macros to make shader struct definition simpler and cleaner and allow
 * a single source of truth definition for both the shader script side structure definition and the
 * corresponding C++ side structure definition with matching memory layout.
 */
namespace xng::ShaderScript
{
    // Maps a C++ scalar type to the corresponding ShaderPrimitiveType::Component enum value.
    template <typename T>
    struct ShaderComponentOf;

    template <>
    struct ShaderComponentOf<bool>
    {
        static constexpr ShaderPrimitiveType::Component value = ShaderPrimitiveType::BOOLEAN;
    };

    template <>
    struct ShaderComponentOf<int>
    {
        static constexpr ShaderPrimitiveType::Component value = ShaderPrimitiveType::SIGNED_INT;
    };

    template <>
    struct ShaderComponentOf<unsigned int>
    {
        static constexpr ShaderPrimitiveType::Component value = ShaderPrimitiveType::UNSIGNED_INT;
    };

    template <>
    struct ShaderComponentOf<float>
    {
        static constexpr ShaderPrimitiveType::Component value = ShaderPrimitiveType::FLOAT;
    };

    template <>
    struct ShaderComponentOf<double>
    {
        static constexpr ShaderPrimitiveType::Component value = ShaderPrimitiveType::DOUBLE;
    };

    // Extracts template parameters from a ShaderDataObject instantiation.
    template <typename T>
    struct ShaderDataObjectTraits;

    template <ShaderPrimitiveType::Type TYPE, ShaderPrimitiveType::Component COMP, size_t C>
    struct ShaderDataObjectTraits<ShaderDataObject<TYPE, COMP, C>>
    {
        static constexpr ShaderPrimitiveType::Type value_type = TYPE;
        static constexpr ShaderPrimitiveType::Component component = COMP;
        static constexpr size_t count = C;
    };

    // Maps a C++ math type to its DSL ShaderDataObject type.
    // Primary: undefined (compile error for unsupported types).
    // DSL struct types (generated by DefineStruct) expose a nested CPU type
    // and are detected by the partial specialization below — they ARE their own DSL type.
    template <typename T, typename = void>
    struct ShaderTypeOf;

    template <>
    struct ShaderTypeOf<bool>
    {
        using type = ShaderDataObject<ShaderPrimitiveType::SCALAR, ShaderPrimitiveType::BOOLEAN, 1>;
    };

    template <>
    struct ShaderTypeOf<int>
    {
        using type = ShaderDataObject<ShaderPrimitiveType::SCALAR, ShaderPrimitiveType::SIGNED_INT, 1>;
    };

    template <>
    struct ShaderTypeOf<unsigned int>
    {
        using type = ShaderDataObject<ShaderPrimitiveType::SCALAR, ShaderPrimitiveType::UNSIGNED_INT, 1>;
    };

    template <>
    struct ShaderTypeOf<float>
    {
        using type = ShaderDataObject<ShaderPrimitiveType::SCALAR, ShaderPrimitiveType::FLOAT, 1>;
    };

    template <>
    struct ShaderTypeOf<double>
    {
        using type = ShaderDataObject<ShaderPrimitiveType::SCALAR, ShaderPrimitiveType::DOUBLE, 1>;
    };

    template <typename T>
    struct ShaderTypeOf<Vector2<T>>
    {
        using type = ShaderDataObject<ShaderPrimitiveType::VECTOR2, ShaderComponentOf<T>::value, 1>;
    };

    template <typename T>
    struct ShaderTypeOf<Vector3<T>>
    {
        using type = ShaderDataObject<ShaderPrimitiveType::VECTOR3, ShaderComponentOf<T>::value, 1>;
    };

    template <typename T>
    struct ShaderTypeOf<Vector4<T>>
    {
        using type = ShaderDataObject<ShaderPrimitiveType::VECTOR4, ShaderComponentOf<T>::value, 1>;
    };

    template <typename T>
    struct ShaderTypeOf<Matrix<T, 2, 2>>
    {
        using type = ShaderDataObject<ShaderPrimitiveType::MAT2, ShaderComponentOf<T>::value, 1>;
    };

    template <typename T>
    struct ShaderTypeOf<Matrix<T, 3, 3>>
    {
        using type = ShaderDataObject<ShaderPrimitiveType::MAT3, ShaderComponentOf<T>::value, 1>;
    };

    template <typename T>
    struct ShaderTypeOf<Matrix<T, 4, 4>>
    {
        using type = ShaderDataObject<ShaderPrimitiveType::MAT4, ShaderComponentOf<T>::value, 1>;
    };

    template <typename T, size_t N>
    struct ShaderTypeOf<std::array<T, N>>
    {
    private:
        using traits = ShaderDataObjectTraits<typename ShaderTypeOf<T>::type>;

    public:
        using type = ShaderDataObject<traits::value_type, traits::component, (int)N>;
    };

    // DSL struct types: detected by presence of nested T::CPU.
    // The struct itself IS the DSL type — no wrapping needed.
    template <typename T>
    struct ShaderTypeOf<T, std::void_t<typename T::CPU>>
    {
        using type = T;
    };

    template <typename T, typename = void>
    struct ShaderCpuTypeOf
    {
        using type = Std140<T>;
    };

    template <typename T>
    struct ShaderCpuTypeOf<T, std::void_t<typename T::CPU>>
    {
        using type = typename T::CPU;
    };

    template <typename T>
    using ShaderCpuType = typename ShaderCpuTypeOf<T>::type;
}

